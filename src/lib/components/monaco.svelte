<script lang="ts">
    import type monaco from 'monaco-editor';
    import { onMount } from 'svelte';
    import editorWorker from 'monaco-editor/esm/vs/editor/editor.worker?worker';
    import jsonWorker from 'monaco-editor/esm/vs/language/json/json.worker?worker';
    import cssWorker from 'monaco-editor/esm/vs/language/css/css.worker?worker';
    import htmlWorker from 'monaco-editor/esm/vs/language/html/html.worker?worker';
    import tsWorker from 'monaco-editor/esm/vs/language/typescript/ts.worker?worker';
    import { languageOptionsByMonaco } from '$lib/enums/languages';
    import type { Writable } from 'svelte/store';
    import lang from '$lib/stores/lang';

    let divEl: HTMLDivElement = null;
    export let editor: Writable<monaco.editor.IStandaloneCodeEditor>;
    let Monaco;

    onMount(async () => {
        // @ts-ignore
        self.MonacoEnvironment = {
            getWorker: function (_moduleId: any, label: string) {
                if (label === 'json') {
                    return new jsonWorker();
                }
                if (label === 'css' || label === 'scss' || label === 'less') {
                    return new cssWorker();
                }
                if (label === 'html' || label === 'handlebars' || label === 'razor') {
                    return new htmlWorker();
                }
                if (label === 'typescript' || label === 'javascript') {
                    return new tsWorker();
                }
                return new editorWorker();
            }
        };

        Monaco = await import('monaco-editor');
        editor.set(Monaco.editor.create(divEl, {
            value: ['#include <bits/stdc++.h>\nint main() {', '\tcout<<"Hello World!"<<endl;', '}'].join('\n'),
            language: 'cpp',
            theme: 'vs-dark',
        }));
        lang.subscribe((ln) => {
            $editor.setModel(Monaco.editor.createModel($editor.getValue(), languageOptionsByMonaco[ln]));
        });

        return () => {
            $editor.dispose();
        };
    });
</script>

<div bind:this={divEl} class="h-full w-full" />